#!/usr/bin/env python3
"""Build the published جموع التكسير dataset from the expert-revised master sheet.

Input  : sources/الجدول-الموحد المعدل.xlsx   (revised by the reviewer; still has الفروق)
Output : data/csv/جموع التكسير.csv
         data/xlsx/جموع التكسير.xlsx
         data/json/جموع التكسير.json
         site/assets/data.js               (same rows, embedded for the static site)

The الفروق (differences) column is dropped, as requested: it documented how the
two source books disagreed and is no longer needed now that the table is verified.
Rows are re-sorted alphabetically by singular pattern, then plural, then type.
"""
import csv
import json
import os
import re
from datetime import date

import openpyxl
from openpyxl.styles import Alignment, Font, PatternFill

from parse_examples import parse as parse_examples

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
SRC = os.path.join(ROOT, "sources", "الجدول-الموحد المعدل.xlsx")

RLM, LRM = "‏", "‎"
# Arabic diacritics + tatweel, stripped only when building sort keys.
DIACRITICS = "".join(chr(c) for c in range(0x064B, 0x0653)) + "ٰـ"
DEDIAC = str.maketrans("", "", DIACRITICS)

HEADERS = ["المفرد", "النوع", "الجمع", "الأمثلة"]
KEYS = ["singular", "type", "plural", "examples"]  # ascii keys (json carries arabic)


def clean(s):
    """Semantic value: drop bidi marks, collapse whitespace."""
    if s is None:
        return ""
    return re.sub(r"\s+", " ", s.replace(RLM, "").replace(LRM, "")).strip()


def rtl_fix(s):
    """Force RTL base direction for cells not starting with a strong RTL letter,
    so leading '(' or '-' and trailing '...' render on the correct side."""
    for ch in s:
        if ch.isspace():
            continue
        if "؀" <= ch <= "ۿ" or "ݐ" <= ch <= "ݿ":
            return s
        return RLM + s
    return s


def sort_key(row):
    sing, typ, plur = row[0], row[1], row[2]
    return (sing.translate(DEDIAC), sing, plur.translate(DEDIAC), plur, typ)


def load_rows():
    wb = openpyxl.load_workbook(SRC)
    ws = wb.active
    rows = []
    for r in range(2, ws.max_row + 1):
        sing = clean(ws.cell(row=r, column=1).value)
        typ = clean(ws.cell(row=r, column=2).value)
        plur = clean(ws.cell(row=r, column=3).value)
        ex = clean(ws.cell(row=r, column=4).value)
        if not sing or not plur:
            continue  # skip blank rows
        rows.append([sing, typ, plur, ex])
    rows.sort(key=sort_key)
    return rows


def write_csv(rows, path):
    with open(path, "w", encoding="utf-8-sig", newline="") as f:
        w = csv.writer(f)
        w.writerow(HEADERS)
        w.writerows([rtl_fix(c) for c in row] for row in rows)


def write_xlsx(rows, path):
    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = "جموع التكسير"
    ws.sheet_view.rightToLeft = True
    ws.append(HEADERS)
    for row in rows:
        ws.append([rtl_fix(c) for c in row])
    header_fill = PatternFill("solid", fgColor="1F6F54")
    bold_white = Font(bold=True, color="FFFFFF")
    for cell in ws[1]:
        cell.font = bold_white
        cell.fill = header_fill
        cell.alignment = Alignment(horizontal="center", vertical="center",
                                   wrap_text=True, readingOrder=2)
    wrap = Alignment(vertical="top", wrap_text=True, readingOrder=2)
    for row in ws.iter_rows(min_row=2):
        for cell in row:
            cell.alignment = wrap
    for i, wdt in enumerate([16, 10, 16, 95], start=1):
        ws.column_dimensions[openpyxl.utils.get_column_letter(i)].width = wdt
    ws.freeze_panes = "A2"
    wb.save(path)


def ascii_blocks(blocks):
    """Arabic-keyed example blocks -> compact ascii-keyed blocks for the site."""
    out = []
    for b in blocks:
        ob = {"cond": b["شرط"], "irregular": b["شاذ"],
              "pairs": [{"s": p[0], "p": p[1]} for p in b["أزواج"]]}
        if b.get("ملاحظة"):
            ob["note"] = b["ملاحظة"]
        out.append(ob)
    return out


def write_json(rows, path):
    data = []
    for row in rows:
        rec = dict(zip(HEADERS, row))
        rec["أمثلة"] = parse_examples(row[3])  # structured example blocks
        data.append(rec)
    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)


def write_site_data(rows, path):
    site_rows = []
    for row in rows:
        rec = dict(zip(KEYS, row))
        rec["blocks"] = ascii_blocks(parse_examples(row[3]))
        site_rows.append(rec)
    payload = {
        "generated": date.today().isoformat(),
        "count": len(rows),
        "rows": site_rows,
    }
    blob = json.dumps(payload, ensure_ascii=False, indent=0)
    with open(path, "w", encoding="utf-8") as f:
        f.write("// Auto-generated by scripts/build_dataset.py — do not edit by hand.\n")
        f.write("window.PLURALS = ")
        f.write(blob)
        f.write(";\n")


def main():
    rows = load_rows()
    singulars = sorted({r[0] for r in rows})
    plurals = sorted({r[2] for r in rows})
    pairs = {(r[0], r[2]) for r in rows}

    write_csv(rows, os.path.join(ROOT, "data", "csv", "جموع التكسير.csv"))
    write_xlsx(rows, os.path.join(ROOT, "data", "xlsx", "جموع التكسير.xlsx"))
    write_json(rows, os.path.join(ROOT, "data", "json", "جموع التكسير.json"))
    write_site_data(rows, os.path.join(ROOT, "site", "assets", "data.js"))

    print(f"rows            : {len(rows)}")
    print(f"singular patterns: {len(singulars)}")
    print(f"plural patterns  : {len(plurals)}")
    print(f"distinct sing→plur edges: {len(pairs)}")
    print("wrote data/{csv,json,xlsx} + site/assets/data.js")


if __name__ == "__main__":
    main()
